public class Perulangan {
    public static void main(String[] args) {
        // For
        for (int i = 1; i <= 10; i++) {
            System.out.println("Perulangan ke " + i);
        }

        // While
        int i = 1;
        while (i <= 10) {
            System.out.println("Perulangan ke " + i);
            i++;
        }
    }
}
