public class Aritmatika {
    public static void main(String[] args) {
        int number1 = 120;
        int number2 = 40;

        System.out.println(number1 + number2);
        System.out.println(number1 - number2);
        System.out.println(number1 * number2);
        System.out.println(number1 / number2);
        System.out.println(number1 % number2);

        // Augmented Assigment
        int a = 50;
        System.out.println(a += 80);
        System.out.println(a -= 80);
        System.out.println(a *= 80);
        System.out.println(a /= 80);
        System.out.println(a %= 80);
    }
}
