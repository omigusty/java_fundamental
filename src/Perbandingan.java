public class Perbandingan {
    public static void main(String[] args) {
        int number1 = 100;
        int number2 = 100;

        System.out.println(number1 == number2);
        System.out.println(number1 != number2);
        System.out.println(number1 < number2);
        System.out.println(number1 > number2);
        System.out.println(number1 <= number2);
        System.out.println(number1 >= number2);
    }
}
